
/* PERULANGAN DENGAN WHILE */
fun C(){
    var index = 1
    while(index <= 10) {
        println("Looping ke Index $index")
        index++
    }
}

/* PERULANGAN DENGAN DO WHILE */
fun D(){
    var index = 1
    do{
        println("Peulangan Ke $index")
        index++
    } while(index <= 10)
}